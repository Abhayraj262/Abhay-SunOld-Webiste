            <p class=""> A BEE ( bureau of energy efficiency )  -rating certificate is a document that indicates the product’s level of BEE compliance. A level one contributor is the highest achievable status and the lowest level of BEE compliance is level eight. The higher the BEE status level, the stronger a company is in relation to its competition. Companies should aim to achieve a higher status level annually.  </p><br>

            <h6>ABOUT THE BEE STAR RATING PROGRAM</h6><br>
           <p>BEE sets the standards and label for marking star-rating of various appliances( BEE label on electronics ). They also define the testing and certification procedures for all kinds of appliances. When manufacturers make a new model and they want to get their product certified, they do the tests for their appliance as per the procedures designed by BEE and apply for star certification with the data from the tests. Based on the test data BEE provides a star rating for appliances. BEE keeps on refining the standards and certification procedures periodically. </p><br>
           <h6>WHY DOES A PRODUCT NEED BEE-RATING CERTIFICATE ?</h6><br>
           <p>Product needs a BEE-rating certificate to prove their level of performance and electricity consumption status. The certificate includes indicators about product efficiency and performance as per electricity consumption.

            The certificate is valid for a year and renewed annually based on evidence submitted for BEE implementation during the period under measurement. Companies can use their BEE-rating certificate to claim product quality among other suppliers. If you don’t have a BEE-rating certificate, your customers will look for a BEE compliant supplier. It is important for companies to aim to achieve and maintain a level one status to remain competitive.
          <br>Stickers issued by BEE are to be pasted on the product certified to depict star rating on electrical appliances.
          </p>

          
          


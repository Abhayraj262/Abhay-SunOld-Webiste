          <p>BIS Stands for Bureau of Indian Standards and CRS stands for Compulsory Registration Scheme. CRS
            registration is a product registration scheme under BIS which was originally launched for Electronic goods
            under which later on Electrical and other types of goods also have been included.BIS is the implementation
            body for the quality control orders known as Gazette notifications released by various ministries of
            government of India.</p>

          <p>government of India.
            First quality control order for CRS certification was released for implementation in the year 2012 and since
            then the products have been added in a phase-wise manner in this scheme over the years. After the recent
            notifications more than 80 products under various Indian Standards are covered under BIS CRS registration
            procedure.
          </p>


          <h4 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;" id="2">Products
            Covered under Compulsory Registration Scheme.</h4>
          <p>BIS CRS certification can only be taken in case of mandatory products, unlike ISI mark it cannot be taken
            on products voluntarily for which no QCO’s have been released.
            Products covered under CRS scheme can be accessed from the link below to BIS website <a
              href="https://www.crsbis.in/BIS/products-bis.do" style="color:blue !important;"> on this link</a>
          </p>

          <h4 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;" id="3">Eligibility
            Criteria for the BIS CRS Registration.
          </h4>
          <p>Below are the eligibility criterias for applying for CRS registration :
          </p>

          <p> 1. The applicant must be a manufacturer , can be of Indian or foreign origin.<br>
            2.In case of foreign manufacturer, the applicant must have an Authorized Indian Representative appointed (
            AIR ), Which can be their own branch office, or another company authorized by them.
            <br>
            3.Test report must be available from a BIS approved laboratory as per the Indian Standard ( IS ) applicable
            on the product.
          </p>

          <h4 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;" id="4"> Documents
            Required ✔
          </h4>
          <p>The following documents are required for obtaining of CRS certification :</p>


          <p> 1. Address proof of the manufacturer.<br>
            2. Test report from BIS recognized test lab.<br>
            3. Undertakings as per BIS formats<br>
            4. Trademark certificate ( if the brand is registered ).<br>
            5. Agreement between the foreign manufacturer and AIR ( in case of import ) <br>
            6. Affidavit in the applicable format <br>
            7. Fee receipt, etc,</p>

          <h4 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;" id="5">Procedure
          </h4>
          <p>Obtaining a BIS CRS registration certificate is completely an online procedure. The steps involved in the
            same are as follows:
          </p>

          <p>
            - Getting the product tested as per Indian Standard ( IS ) applicable on the product from a BIS approved
            laboratory. <br>
            - Preparation of documents as per BIS guidelines <br>
            - Payment of the requisite fees as per the fee structure <br>
            - Filing of the online application. <br>
            - Clearing of queries on the application if any raised by the department. <br>
            - Grant of registration number. <br>
            - Mentioning the registration number along with Indian Standard and logo provided by BIS.

          </p>
          <p>Timeline : The certificate can be obtained in approx 4 weeks from the date of application.</p>
          <p>Fees Structure: <br>
            1.) Government Fees: INR 53,000+GST@18% per report ( INR 20,000+GST@18% extra for every additional report in
            1 application , or as as applicable case to case ) <br>
            2.) Testing charges : Approx INR 10-20,000+GST@18% ( Depends on the product to be tested.)

          </p>
          <p>Validity : Initially CRS certificate is granted for 2 years and after that it can be renewed upto 5 years
            by paying the requisite fee online.
          </p>

          <h4 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;" id="6">Guidelines
            for E-Labelling of the products under the BIS CRS Registration Scheme:</h4>
          <p>The requirements for e-labelling are as given below:
          </p>

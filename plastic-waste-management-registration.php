          <p> plastic waste management
          </p>
          <p>The initiative aims to establish environment-friendly plastic waste disposal solutions.
            In the process it seeks to ban the use of plastic bags and plastic products, and reduce plastic littering
            across the state.

            It has been observed that disposal of plastic waste is a serious concern due to improper collection
            and segregation system.
            Only 60% of the plastic produced is recycled, balance 9400 Tones of plastic is left unattended in
            environment causing land, air and water pollution
          </p>


          <h2 class="h1-design heading_design font-size m-t10 text-center" style="text-transform: uppercase;" id="2">
            Who can Apply
          </h2>
          <p>All manufacturers, either of Indian or foreign origin can apply for the <span style="color:blue">Plastic
              licence</span>.In case of foreign manufacturer license will be granted to the manufacturer and the Indian
            counterpart of the manufacturer will act as it’s Indian Representative. The Indian representative of the
            manufacturer must have a valid state drugs license under Form 20-B/21-B or Form 42 ( applied under Form 41 )
            as per the latest guidelines released by CDSCO.
          </p>

          <h2 class="h1-design heading_design m-t10 font-size text-center" style="text-transform: uppercase;" id="3">WHAT ARE THE
            BUSINESSES REQUIRING IN CERTIFICATION?:</h2>

          <p>1. Units Engaged in Processing or Recycling of Plastic Waste
          </p>
          <p>2. Producers or Brand Owners of plastic products
          </p>
          <p>3. Manufacturers of Plastic Raw Materials
          </p>


          <h2 class="h1-design heading_design font-size m-t10 text-center" style="text-transform: uppercase;" id="4"> WHY PLASTIC
            WASTE MANAGEMENT CERTIFICATE IS REQUIRED :
          </h2>
          <p>The Plastic Waste Management Rules, 2016 make an arrangement to reduce the negative impact on the
            environment-related to plastic waste, if we talk about these days scenario plastic is a vital material and
            almost impossible to ban. Under the PWM rules, authorities such Urban Local Bodies should provide separate
            collection, storage, and processing of plastic waste in respected areas.</p>

          </p>


          <p> 1. Plastic has reached in a rural area and therefore it expands the jurisdiction of applicability from the
            municipal area to rural areas <br>
            2. To set responsibilities to producer and generator of plastic waste to take back waste from the market in
            respect of their target as Extended producer responsibility (EPR) <br>
            3. To promote use of plastic waste for road construction as per Indian Road Congress guidelines or energy
            recovery, or waste to oil etc. <br>
            4. Monitor and facilitate to use of more than 50 micron plastic carry bag <br>
            5. To create awareness in the civil societies to minimize the use of plastic , etc.
          </p>

          <h2 class="h1-design heading_design font-size m-t10 text-center" style="text-transform: uppercase;" id="5">What are the
            documents Required for Certificate
          </h2>

          <p>
          <ul>
            <li>1. Authorization Letter (except Proprietorship)</li>
            <li> 2. Layout Plan</li>
            <li> 3. Agreement with Authorized Recycler</li>
            <li> 4. Rent Agreement/Proof of Location</li>
            <li> 5. Pan card and Aadhar Card of Authorized person</li>
            <li> 6. Consent to Operate (CTO) from State Pollution Control Authority</li>
            <li> 7. Cost of project</li>
          </ul>
        
          <p>Timeline : Approx 6-8 weeks after submission of application.
          </p>

          <p>Validity : It is granted for 5 years initially and has to be renewed after that by paying the government
            fees.
          </p>


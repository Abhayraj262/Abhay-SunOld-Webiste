            <p>The management of plastic garbage is the problem, not plastic as a substance. India produces 26000 tonnes of plastic garbage each day from metropolitan areas, according to numerous surveys. Plastic garbage generated by land-based activities has a harmful influence on the ecosystem, particularly on marine life. Plastic is a special material with a wide range of uses, including the packaging of food products as well as industrial and medical equipment. </p>
            <p>According to the 2016 Plastic Waste Management also known as PWM Rules, Urban Local Bodies (ULBs) must ensure that no one uses plastic bags that are less than 50 microns thick and that recycled plastics are not used to package food or beverages. Additionally, the Rules mandate that local entities offer separate collecting, storage, and processing of waste in their region. </p>


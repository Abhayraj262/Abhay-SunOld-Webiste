  <script>
        const slider = document.querySelector('.slider');
        const products = document.querySelectorAll('.product');
        const totalProducts = products.length;
        let currentIndex = 0;
        let autoSlide;

        const startAutoSlide = () => {
            autoSlide = setInterval(() => {
                currentIndex = (currentIndex + 1) % Math.ceil(totalProducts / 1);
                updateSlider();
            }, 4000);
        };

        const stopAutoSlide = () => {
            clearInterval(autoSlide);
        };

        const updateSlider = () => {
            const itemWidth = products[0].offsetWidth + 10; // Include margin
            slider.style.transform = `translateX(-${itemWidth * 1 * currentIndex}px)`;
        };

        slider.addEventListener('click', stopAutoSlide);
        document.body.addEventListener('click', (e) => {
            if (!e.target.closest('.slider')) {
                startAutoSlide();
            }
        });

        // Pause slider on image hover
        products.forEach(product => {
            const image = product.querySelector('img');
            image.addEventListener('mouseover', stopAutoSlide);
            image.addEventListener('mouseout', startAutoSlide);
        });

        window.addEventListener('resize', updateSlider);

        // Initialize slider
        startAutoSlide();
    </script>





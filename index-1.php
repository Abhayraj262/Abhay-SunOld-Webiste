    function validateForm() {
        var name = document.getElementById('h-name').value.trim();
        var email = document.getElementById('h-email').value.trim();
        var phone = document.getElementById('h-phone').value.trim();
        var message = document.getElementById('h-message').value.trim();
        
        // Validate Name
        if (name === "") {
            alert("Please enter your Name.");
            return false;
        }
        
        // Validate Email
        if (email === "") {
            alert("Please enter your Email.");
            return false;
        } else {
            // Regular expression to validate email format
            var emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            if (!emailRegex.test(email)) {
                alert("Please enter a valid Email address.");
                return false;
            }
        }
        
        // Validate Phone Number
        if (phone === "") {
            alert("Please enter your Mobile Number.");
            return false;
        } else {
            // Regular expression to validate 10-digit Indian mobile number starting with 6-9
            var phoneRegex = /^[6-9]\d{9}$/;
            if (!phoneRegex.test(phone)) {
                alert("Please enter a valid 10-digit Mobile Number starting with 6-9.");
                return false;
            }
        }
        
        // Validate Message (optional)
        if (message.length > 70) {
            alert("Message should not exceed 70 characters.");
            return false;
        }
        
        // Form is valid
        return true;
    }
</script>

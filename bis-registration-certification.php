              <h2 class="h1-design font-size heading_design m-t10" id="2">
          What is BIS or Bureau of Indian Standards or BIS Certification full form?
          </h2>
            <p>BIS’ full form is Bureau of Indian Standard, an Indian National Standard Body established in 1986.
            It was created to harmonize the quality of consumer and industrial products being sold in India . BIS 
            Certification or BIS License on a product ensures its quality and that the product is as per BIS Standardswhich
            are set by the Indian government to meet international quality standards. To obtain BIS certificate in India the 
            manufacturer must first identify in which scheme of BIS the product is falling and then follow the procedure to
            obtain the BIS Certificate so that their products can be sold in India.
            </p>
            <p>
                BIS product certification ensures integrity, trustworthiness, and reliability of any product.
                To apply for BIS certification the applicant has to be a manufacturer, either of Indian or foreign origin. 
                In some cases BIS registration can be optional or mandatory depending upon the nature of the product and the  BIS scheme in which it lies.
            </p>
          
        <h2 class="h1-design font-size  m-t10 " style="color:#03685d" id="1">
           Despite being optional in some cases, the government promotes obtaining BIS Certificate due to the following reasons:-
          </h2>
    

            <ul class="pt-2">
            <li>It protects the general public's health</li>
            <li>It offers quality control at the manufacturer's end</li>
            <li>It safeguards consumers from dangerous goods</li>
            <li>It increases consumer trust on the standard marked products</li>
          </ul>

            <h2 class="h1-design font-size heading_design m-t10 text-center" style="text-transform: uppercase;" id="2">
             Advantages of BIS certificate or BIS Licence are as follows

            </h2>
            <p>
            <ul>
              <li>Ensures high levels of quality because goods with a BIS registration are produced per BIS standards and are tested during the process.
              </li>
              <li>If the products are marked with BIS license number, it ensures that no harmful chemicals / components are being used in consumer products.</li>
              <li>Gives market edge over the manufacturers who are not certified.
              </li>
              <li>Helps Indian manufacturers to export the product, due to their products being developed and tested as per ISS ( Indian Standard Specifications ) which are at par with international standards.
              </li>
              <li>Enables Foreign manufacturers to understand requirements of the Indian market and standards so that they can develop the products according to the BIS product certification regulations.
              </li>
            </ul>
            </p>

            <h2 class="h1-design heading_design font-size m-t10 text-center" style="text-transform: uppercase;" id="4">Schemes under the purview of BIS are as follows:

            </h2>
            <p><strong>1. </strong> BIS License for Indian manufacturers (For obtaining BIS certification on electrical, Metal, Industrial, and other types of products.)</p>
              <p><strong>2. </strong> BIS License for Foreign Manufacturers / Indian Importers under FMCS (For obtaining BIS License on electrical, Metal, Industrial, and other types of products.)</p>
              <p><strong>3. </strong> BIS Certificate under Compulsory Registration Scheme - CRS (For obtaining BIS registration for electronic products)</p>
              <p><strong>4. </strong> Recognition of Hallmarking Centres</p>
              <p><strong>5. </strong> Hallmark for all types of gold and silver jewellery</p>
              <p><strong>6. </strong> ECO Mark Certification</p>

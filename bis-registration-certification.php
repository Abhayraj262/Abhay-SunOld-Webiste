              <p><strong>8. </strong> Management System Certification Scheme (MSCS)</p>
              <p><strong>9. </strong> Development of BIS Standards for new products.</p>


     <h2 class="h1-design font-size heading_design m-t10 text-center" style="text-transform: uppercase;" id="5">
          Elaboration of each of the BIS schemes is as below
          </h2>
            <h5 class="pt-1" style="color:#03685d"><b>1. BIS License for Indian manufacturers:</b></h5>
            <p>BIS Certification or ISI Mark is granted to Indian Manufacturers under schedule II
            and scheme I of BIS also known as the Product Certification Scheme’s. Currently there are more than 650 products under
            this scheme which have to mandatorily undergo this license procedure to be manufactured in India. More
            than 40,000 manufacturers based in India, have already obtained BIS Certificate for various products like steel, copper, glass,
            footwear, toys, coolers, aluminium products, fans, chemicals, etc. BIS License is initially granted for 1 year and after that it 
            can be renewed from 1-5 years, by payment of the requisite fees.
            To check the products that require mandatory BIS certification for Indian manufacturers , 
            </p>
            
           <h5 class="pt-1" style="color:#03685d"><b>2 BIS Licence for Foreign Manufacturers / Indian Importers under FMCS</b></h5>
           <p>FMCS Standards for Foreign Manufacturer Certification Scheme, under this scheme ISI Mark Certification is granted to 
           manufacturers based out of India who wish to export their products to India. It is governed by BIS Act 2016, 
           and is only granted to manufacturers and not importers. Importers can only become the Authorized Indian Representatives 
           ( AIR ) of the manufacturers but cannot independently obtain the BIS Licence. Currently, there are more than 1850 licenses granted
           to foreign manufacturer since year 2000 under various standards.</p>
           
            <h5 class="pt-1" style="color:#03685d"><b>3. BIS Certificate under Compulsory Registration Scheme ( CRS ) for Indian and Foreign Manufacturers:</b></h5>
            
           <p>Most of the electronic products like mobile phones, adaptors, televisions, li-ion batteries, UPS, LED bulbs, etc
           require BIS registration under the CRS scheme. In which, the product has to be tested as per the BIS standards and the statutory documents of the manufacturer have to be filed along with the application, once the registration is granted R.no.
           is generated which has to be mentioned on the products that are registered with BIS.</p>
           
             <h5 class="pt-1" style="color:#03685d"><b>4. Recognition of Hallmarking Centres</b></h5>
             <p>All the gold and silver jewellery sold in India must have hallmark certification from BIS, and hallmark is 
             granted after the product is tested in a BIS approved Hallmarking centre, therefore it is the duty of BIS to
             recognize hallmarking centres that can test as per the BIS standards.</p>
            <h5 class="pt-1" style="color:#03685d"><b>5.	Hallmark for all types of gold and silver jewellery</b></h5>
            <p>BIS formulates standards as per the international norms according to which all types of gold and 
            silver jewellery must be tested. Hallmark helps the consumer to ensure the purity of product that they 
            are buying and also helps them to know what they are paying for.</p>
               <h5 class="pt-1" style="color:#03685d"><b>6.	ECO Mark Certificatio</b>n</h5>
              <p>
                  The products carrying ECO mark by BIS ensures that they are environmentally safe and are not polluting earth.
                  The major product heads covered under this BIS registration scheme are Architectural paints and powder coating, Beverages, Infant foods, processed fruits and vegetable products, coir and coir products, cosmetics, dry cell batteries & automotive lead acid batteries, edible oils, 
                  tea and coffee, electrical and electronic goods, finished leather, fire extinguishers and food additives.
              </p>
              <h5 class="pt-1" style="color:#03685d"><b>7. BIS Laboratory Recognition Scheme ( LRS )</b></h5>
              <p>
                  As process for obtaining BIS certificate or BIS registration requires testing during the procedure, BIS has 
                  approved numerous laboratories which tests the products as per BIS standards, to confirm the products quality.
                  BIS certification is granted after the test report from BIS laboratory is submitted to the concerned branch office or headquarters,
                  however the case maybe. Laboratories that require BIS certification must have prior approval from NABL.
              </p>
              <h5 class="pt-1" style="color:#03685d"><b>8. Management System Certification Scheme:</b></h5>
              
              <p>This scheme for BIS Certification of management systems was started in the year 1991, and BIS is the only body in India,
              that can grant this certification under the parliament act. The department responsible for granting this BIS certificate is 
              located at the BIS headquarters based in Delhi.</p>
              
               <h5 class="pt-1" style="color:#03685d"><b>9. Development of BIS Standards for new products.</b></h5>
              
              <p>For obtaining BIS certification testing of the products is mandatory and as there are thousands of criteria of tests
              that may be applicable across wide array of products, therefor each product is to be tested as per specific Indian 
              Standard which is developed by Bureau of Indian Standards ( BIS. ) Currently, there are more than 20,000 BIS Standards
              developed by various sectional committees incorporated within the department.</p>
              
              
              
            
            
            

          
           


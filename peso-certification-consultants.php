              slide(e.target).down(function() {

                clicked = false;

                arr[i - 1] = e.target;

                console.log('slide down of accordions ' + i + ' is done');

              }, timer);
            }
          }
        });

        i++;

        if (i < menu.length) {
          accordionsInner(i);
        }

      }
      accordionsInner(0);
    })();

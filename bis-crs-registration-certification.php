      <p class="h7">Guidelines for E-Labelling of the products under the BIS CRS Registration Scheme</p>
      <p>The requirements for e-labelling are as given below: </p>
      <p><b>1.</b> Devices utilizing e-labels shall have a physical label on the packaging of the product at the time of import, storage for sale and sale or distribution. For devices in bulk and not packaged individually, a removable adhesive label on the packaging is acceptable. </p>
      <p><b>2.</b> Devices shall not require any special accessory/tool or supplemental plug-in (e.g., the installation of a SIM/USIM card) to access the e-label. </p>
      <p><b>3.</b> The compliance related information shall be programmed by the responsible party and the information shall be secured in such a manner that third-party cannot modify it. The information can be in the firmware or software menu provided it is easily accessible and cannot be modified</p>
      <p><b>4.</b> Users shall be able to access the information without requiring special access codes or permissions and, in all cases the information shall be accessible in no more than four steps in a device’s menu. </p>
      <p><b>5.</b> Users shall be provided specific instructions on how to access the information. The instructions shall be included in the user’s manual, operating instructions, or as an insert in the package of the product, or other similar means. </p>
      <p><b>6.</b> Alternately, the instructions to access the information may be available on the product related website. The instructions on how to access the website shall be provided in the user’s manual or package of the product. </p>
      <p>
        <b></b>7.</b> All the applicable regulatory information required on the packaging or user manual shall be provided according to the applicable rules even if it is displayed electronically. 
      </p>
      <p><b>8.</b> If the primary user manual or user guide is provided by other electronic media (e.g., CD or online access) this information may also be provided as part of e-label. 
      </p>
      <p>
        <b>9. </b>The “Standard Mark” displayed electronically shall meet all the relevant requirements of the guidelines issued by BIS vide circular reference CMD 3/8:1/6975 dated 3 Dec 2015.
      </p>
              

           <h2>Mandatory Testing and Certification of Telecom Equipments (MTCTE)</h2>
                <p>The Indian Telegraph (Amendment) Rules, 2017, provides that every telecom equipment must undergo mandatory testing and certification prior to sale, import of use in India. The final detailed procedure for Mandatory Testing and Certification of Telecom Equipments (MTCTE) under these rules has been notified separately. The testing is to be carried out for conformance to Essential Requirements for the equipment, by Indian Accredited Labs designated by TEC and based upon their test reports, certificate shall be issued by TEC.
                </p>
                
                <h5 id="1">MTCTE related updates/ clarifications:
    
                </h5>
    
    
                <p>1.) It is clarified that COTS servers are presently not covered by MTCTE. Application Server and Media Server, for which ERs are put on hold are also not covered by MTCTE. However, as and when notification related to Security Requirements for any equipment is issued, the equipment will come (with proper notification) within purview of MTCTE irrespective of it being dedicated hardware based or server based. <br>
                    2.) It is also clarified that Mobile Handsets including Tablets (with or without SIM) are within the purview of MTCTE. Any change in this status like de-notification from CRS list and related detailed modalities shall be notified after these are settled between DoT and MEITY. <br>
                    3.) The MTCTE portal is available for trial. Formal notification of dates from which applications shall be accepted on portal shall be notified separately. For trial, user may change the fee amount from indicated value to ₹ 1 and proceed. <br>
                    4.) Clinical Trial on medical devices division <br>
                    5.) If a user completes his application and report upload in full on the trial portal, he may inform at help.mtcte.tec@gov.in. A trial certificate will be issued for the product. <br>
                    6.) The updated TEC Logo has been uploaded on https://www.mtcte.tec.gov.in/downloads. Please refer to the logo for labeling requirements of equipments under MTCTE.
                    </p>
                
                
                

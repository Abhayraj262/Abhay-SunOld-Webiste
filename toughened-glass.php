          <p> Toughened glass, or safety glass, or tempered glass is ordinary float glass that has been made force-resistant and hence, this type of glass is six times as hard to break as ordinary glass panes. Moreover, if the glass does indeed break, it will break into small, blunt, cube-shaped pieces due to stress patterns within the glass. In a nutshell, nobody gets hurt.</p>

          <h6>How is Toughened Glass Made?</h6>
          There are mainly two ways in which glass can be toughened –
          <h6>Tempering Glass with Heat and Cold</h6>
          <p>Most toughened glass or safety glass is made by tempering, a process in which ordinary glass is heated up to a temperature of around 620°C for some time followed by abrupt cooling using jets of cold air. As a result of this process, the outside of the glass is forced into compression while the inside remains free to float for some time (creating tension on the inside). The higher the thermal expansion of the glass and the lower its thermal conductivity, the higher will be the level of residue stresses, and as a result of this, the stronger will be the resulting glass.</p>

          <h6>Tempering Glass with Chemical Reactions</h6>
          <p> Normal float glass is submerged in a bath of potassium salt at 300°C. When the salt dissolves, it is indicative of its separating into ions. The potassium ions are now free to replace sodium ions in the surface of the glass. Potassium ions are 30% larger than sodium ions; the process brings the outer surface under compression while the inner surface remains the same. This creates a tension inside and now the force required to break the glass has to be much greater (more than the compressive forces). Hence, this method aims at strengthening the glass by chemical ion exchange.</p>


          <h2 class="h1-design heading_design font-size m-t10 text-center">Wireless Planning and Coordination (WPC)</h2>
          <p>Products with radio and/or wireless functionalities which are imported or manufactured and marketed in India must have an equipment type approval (ETA) certificate from the Wireless Planning and Coordination (WPC) wing of the Ministry of Communications of the government of India.This includes products with Bluetooth, wireless local area network access technology (Wi Fi), small digital radios (Zigbee), and radio frequency identification (RFID) among others.
          </p>

          <P>
            <img src="img/New folder/WPC-1.jpg" class="img-fluid" alt="WPC License">
          </P>


          <p>WPC Licence in India is required on almost all the wireless devices operating on mainly Bluetooth and WiFi. Wireless devices WPC licence is issued under the guidelines of Department of telecom and in some cases ETA approval for wireless devices is also required apart from WPC
          </p>
          <h2 class="h1-design font-size heading_design m-t10 text-center" style="text-transform: uppercase;" id="2">
            PRODUCTS COVERED
          </h2>
          <p>Mandatory requirements for all wireless products (transceivers, transmitters and receivers) entering the Indian market. Note: only receivers do not need approval, for example GPS products.
          </p>

          <p><b>Product examples</b>: Wireless Bluetooth-Watches, Wireless Water Heaters, Wireless Home Appliances, Wireless Car Radio, Radio Frequency Identification (RFID) tags and readers, Wireless Medical products used for patient profiling, Laptops, Mobile Phones, Wireless Mouse and Keyboard, Remote Key for entry, Wireless Headphones and/or Earplugs, Wireless Music Players, and any other Radio Frequency Modules</p>




          <h2 class="h1-design font-size heading_design m-t10 text-center" style="text-transform: uppercase;" id="2">
            Equipment Type Approval (ETA)
          </h2>
          <p>Equipment Type Approval (ETA) is an approval that has to be obtained from WPC before importing any wireless device in India which functions in the de-licensed frequency band. Such equipment include products like Bluetooth devices, mobile phones, Wi-Fi technology, RFID and others.
          </p>

          <p>To obtain an ETA for de-licensed frequency band devices, a detailed test report of the equipment obtained from a well-recognized laboratory has to be submitted to WPC for evaluation. We help our clients with testing of their equipment and obtaining the test report in proper format for submission to WPC.</p>



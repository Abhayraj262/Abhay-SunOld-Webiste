          <h4>IS 9873: SAFETY OF TOYS</h4>
          <p>The safety of kids is a sensitive subject, which is mainly close to the heart of every parent. While purchasing toys/products for a kid, product safety remains to be an important issue. Finger traps, sharp edges, loose components, presence of toxic materials and substances which are too flammable can all cause severe damage to the kid and huge fines on the suppliers of any toy/product deemed to be unsafe.</p>
          <h4>IS 9873 Part 1: Mechanical & Physical Properties of Toys</h4>
          <p>The principle of this standard is to diminish risks related to children’s toys. Particular tests are performed on toys based upon the toy type and/or characteristics of the toy. In addition, proper warning labels are addressed on the product in this standard.</p>
          <h4>IS 9873 Part 2: Flammability of Toys</h4>
          <p>This section determines the flammability of toys. It provides general requirements along with exact requirements for particular toys, which come out to be the most harmful, this includes toys to be worn on the head (moustache, wigs, beards, etc.), toy disguise costumes & those intended to be worn by kids during any play, toys intended to be entered by a kid, and soft filled toys</p>
          <h4>IS 9873 Part 3: Migration of Certain Elements</h4>
          <p>This section involves determining the migration of the following elements: Cadmium, Barium, Lead, Mercury, Chromium, Antimony and Selenium from easily reached parts of toys. This applies to the following toy materials: varnishes/ paints/ lacquers/ coatings/ polymeric & similar materials/ printing inks/similar material, textiles, paper and paper board, ceramic/ glass/ metallic materials, other materials whether mass coloured or not, materials intended to leave a trace, (i.e. pencils or liquid pens) and pliable modelling materials.</p>
          <h4>IS 9873 Part 4: Swings, Slides and Similar Activity Toys</h4>
          <p>This section specifies requirements for indoor & outdoor activity toys for domestic family use intended to bear the weight of children under 14 years. This standard covers swings, slides, rocking toys, carousels, climbing frames, inflatable activity toys, paddling pool, motion rides, rung ladders, stepladders, stairways, head & neck entrapment.</p>
          <h4>IS 9873 Part 7: Tests for Finger Paints</h4>
          <p>This part involves requirements for the safety of finger paints only. Finger paints are aqueous, semi-solid or liquid, coloured mixture specially designed for children to apply directly. IS 9873 Part 7 specifies limits that finger paints may contain for listed preservatives, colourants, carcinogenic primary aromatic amines, and migration of certain elements, benzo (a) pyrene, N- Nitrosamines and label warnings.</p>
          <h4>IS 9873 Part 9: Certain Phthalates Esters in Toys</h4>
          <p>This part specifies a maximum acceptable level of certain phthalate esters in toys and children’s products. This standard provides a separate limit for toys meant for children under 4 years of age and other toy products.</p>

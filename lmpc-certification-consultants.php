            <h2 class="h1-design heading_design font-size m-t10 text-center" style="text-transform: uppercase;" id="1">Preview of LMPC certification / LMPC certificate or what is LMPC?
            </h2>
            <p>In India, the import, sale, and distribution of pre-packaged goods are all governed by the <b>Legal Metrology</b> Act 2009 and Legal Metrology Packaged Commodities (LMPC) Rules, 2011 or LMPC certificate. A pre-packaged product is any item assembled in advance and packaged without any customization. LMPC certification guidelines regulates the exchange of items distributed or sold by weight, measure, or number. The Act aims to encourage fair trade practices and safeguard consumer rights by establishing stringent rules and regulations, notably for pre-packaged goods.<b> LMPC certificate’s</b> full form is <b>Legal Metrology</b> Packaged Commodities. Which clearly indicates that LMPC license is applicable on all types of packed items sold in the market and regulates the details mentioned on prouduct's MRP Label.</p>

            <p><b>LMPC product list:</b> Pre-packaged products include, for instance, baby food, gasoline, food items, paint, bagged cement, cables, wires, chemicals, wool, electrical and electronic products, etc.
            </p>
           <p>Trade conflicts are eliminated when the measurement is subject to regulatory monitoring. For instance, if a customer purchases 1 kilogram of lentils, they should receive exactly that amount and nothing less. Legal metrology or a product containing <b> LMPC certificate </b> also ensures catering to international standards using fundamental measuring tools.</p>
            
            <p><b>LMPC certificate registration</b> is generated under rule 27 of the Legal Metrology Packed Commodities ( LMPC ) Rules,2011.It ensures that the product has adequate details on its MRP label to educate the consumers about the product they are willing to purchase. It is granted in three categories, namely:
            </p>
            <p>1.) Importers <br>
              2.) Manufacturer and Packer <br>
              3.) Packer</p>


            <h2 class="h1-design heading_design m-t10 text-center font-size" style="text-transform: uppercase;" id="2">WHAT IS AN LMPC CERTIFICATE OR LMPC LICENSE FOR IMPORTERS?
            </h2>
            <p><b> LMPC registration</b> is applicable to traders who intend to import, manufacturer and re-packers of pre-packaged goods for sale or distribution. They have to apply for <b>LMPC certificate </b> online at the National Single Window System or NSWS.If the application for<b> LMPC registration</b> is approved, the director/controller of legal metrology (the registering authority) will register the importer/manufacturer and packer/packer’s name and address and issue a LMPC certificate. 
            </p>
            <p>LMPC certificate validity is perpetual i.e. it does not have any end date and it is neither mentioned on the certificate received.
            </p>
            <p>
            Earlier the application was to be submitted to the state legal metrology department, now the same has to be applied through<b> LMPC registration </b> online portal named NSWS, and the LMPC certificate issued is valid for sales PAN India.
            </p>
            <p>This transition to an online process for obtaining LMPC certificate has made it more efficient and transparent, reducing the turnaround time to for the<b> LMPC licence</b> . The above details are the same for importers/ manufacturers and packers too. Once the<b> LMPC registration</b> is completed and LMPC certificate is released businesses are </p>

            <p>free to import, manufacture, or pack their pre-packaged goods without facing any legal obstacles in the Indian market from <b>Legal Metrology</b> Department.</p>

            <h2 class="h1-design heading_design font-size m-t10 text-center" style="text-transform: uppercase;" id="3">DETAILS TO BE MENTIONED ON PRODUCTS AS PER LEGAL METROLOGY RULES ARE AS FOLLOWS:
            </h2>
            <p>To ensure that the supplier has enough details on MRP label that is required to educate the consumer about the product, Legal metrology guidelines helps maintain transparency and trust between the supplier and the consumer. Proper labelling is also essential for meeting the requirements of the LMPC certificate, ensuring that the product complies with all legal metrology regulations, below are the guidelines on details to be mentioned after the manufacturer/importer/packer obtains their respective LMPC certificate:
            </p>
            <p>1.Name and address of the manufacturer / importer / packer:  This ensures traceability and accountability of the product's origin and responsible party. Including this information is mandatory under the<b> LMPC registration </b> guidelines.</p>

            <p>2.Name of goods in the package.  <br>
               3.Country of import ( in case of importer ) for imported goods, mentioning the country of origin is a requirement as per the LMPC license rules, providing transparency to the consumer about from where the product has been sourced. <br>
            4.Total quantity with respect to standard units of weights or measurement ( Like Kg , meters, etc.)this helps to avoid quantity related disputes and ensure the compliance of legal metrology act.<br>
          5. Date of import (in case of importer)
            6.Manufacturing Date<br>
            7.Maximum Retail Price (MRP) with rounded up decimals and inclusive of all taxes.<br>
              8.Manufacturing Date<br>
              9.Dimension of goods ( If applicable )<br>
             
              10.Email id and Phone number of a concerned person or office in case of any consumer complaints , etc,
            </p>
            <p>The declaration must adhere to a predetermined structure in order to be presented on the box, whether it is printed in Hindi or English.</p>
            <p>The Legal Metrology Packaged Commodity Rules include fines of up to Rs. 25,000 for violations depending upon the nature of the product and the firm committing the violation of law. Additionally, a late fee will be imposed if an importer requests for an LMPC certificate more than 90 days after beginning imports.
            </p>





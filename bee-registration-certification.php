            <p><h5>BEE Registration Certification</h5>        </p>
                <p>A BEE ( bureau of energy efficiency ) -rating certificate is a document that indicates the product’s level of BEE compliance. A level one contributor is the highest achievable status and the lowest level of BEE compliance is level eight. The higher the BEE status level, the stronger a company is in relation to its competition. Companies should aim to achieve a higher status level annually.


                </p>
                
                <h5 id="1">ABOUT THE BEE STAR RATING PROGRAM
    
                </h5>
    
    
                <p>BEE sets the standards and label for marking star-rating of various appliances( BEE label on electronics ). They also define the testing and certification procedures for all kinds of appliances. When manufacturers make a new model and they want to get their product certified, they do the tests for their appliance as per the procedures designed by BEE and apply for star certification with the data from the tests. Based on the test data BEE provides a star rating for appliances. BEE keeps on refining the standards and certification procedures periodically.
                    </p>
                
                
                
                <h4 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;" id="2">
                    Who can Apply
                </h4>
              <p>All manufacturers, either of Indian or foreign origin can apply for the <span style="color:blue">CDSCO licence</span>.In case of foreign manufacturer license will be granted to the manufacturer and the Indian counterpart of the manufacturer will act as it’s Indian Representative. The Indian representative of the manufacturer must have a valid state drugs license under Form 20-B/21-B or Form 42 ( applied under Form 41 ) as per the latest guidelines released by CDSCO.
            </p>
                
                <h4 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;" id="3">SClassification of medical devices as per regulations:</h4>
                <p>All types of medical devices are divided into 4 classes by the Medical Device Rules 2017, in 4 main categories, namely</p>
    
                <p> 1. Class A ( least risk ) <br>
                    2. Class B ( Moderate risk ) <br>
                    3. Class C ( Moderate to high risk ) <br>
                    4. Class D ( High risk )
                    </p>
    
    
                    <p>Class A products are further divided into 2 categories to ascertain if <span style="color: blue;"> CDSCO license</span> or <span style="color: blue;">CDSCO registration</span> is applicable on the product:
                    </p>
    
                    <p>a.) Class A ( non sterile and non measuring ) products come under registration scheme. <br>
                        b.) Class A ( Sterile or measuring ) products require license.
                        </p>
    
                        <br><br>
                        <p>This brings us to the conclusion that products which are under class A ( sterile or measuring ), Class B, Class C and Class D require a license from CDSCO.
                        </p>
    
    
    
                
               
                    <h4 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;" id="4">WHY DOES A PRODUCT NEED BEE-RATING CERTIFICATE ?i:
                    </h4>
                   
    
                    <p> Product needs a BEE-rating certificate to prove their level of performance and electricity consumption status. The certificate includes indicators about product efficiency and performance as per electricity consumption. The certificate is valid for a year and renewed annually based on evidence submitted for BEE implementation during the period under measurement. Companies can use their BEE-rating certificate to claim product quality among other suppliers. If you don’t have a BEE-rating certificate, your customers will look for a BEE compliant supplier. It is important for companies to aim to achieve and maintain a level one status to remain competitive.
Stickers issued by BEE are to be pasted on the product certified to depict star rating on electrical appliances.
                        </p>
    
                    <h4 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;" id="5">PRODUCTS AND STEPS FOR OBTAINING BEE CERTIFICATION
    </h4>
    
                        <p>
                            <p>PRODUCTS FOR WHICH BEE LABELING IS MANDATORY</h5>
                        </p>
    

    
                        <p><img src="img/env/bee-te.webp" alt="image">
                        </p>
    
                        <p>Validity : Validity of CDSCO online registration is perpetual i.e. no end date and requires no renewal.
                        </p>
    
                        <p>Note : After the registration is completed, a unique file number is generated, which has to be mentioned on the product / packaging, in case of registration there is no certificate</p>
    
               
                  <h4 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;" id="6">Process for CDSCO license :</h4>
                
                  
                  <p>
                    <ul>
                        <li>Identifying the class of the medical device.
                        </li>
                        <li>Making groups as per the guidelines mentioned as per CDSCO guidelines.
                        </li>
                        <li>Creation of login credentials for the manufacturer.
                        </li>
                        <li>Collection of documents from the overseas manufacturer.</li>
                        <li>Collection of documents from Indian importer.
                        </li>
                        <li>Checking and vetting of documents as per the rules.</li>
                        <li>Payment of government fees.
                        </li>
                        <li>Submission of Application online.
                        </li>
                        <li>Clearing of queries ( if any )
                        </li>
                        <li>Audit of the firm.
                        </li>
                        <li>Clearing of queries ( if any ) after the audit.
                        </li>
                        <li>Grant of license
                        </li>
                        <li>Generation of CDSCO certificate.
                        </li>
                    </ul>
                  </p>
                  <p>Timeline : Approx 6-8 weeks after submission of application.
                </p>
                 
                <p>Validity : It is granted for 5 years initially and has to be renewed after that by paying the government fees.
                </p>
    
                <p>Note: 1.) In case of CDSCO import license,  AUDIT OF THE FOREIGN MANUFACTURER IS NOT CONDUCTED, and rest of the procedure remains the same. <br>
                    2.) License number mentioned on the CDSCO registration certificate has to be mentioned on the products / packaging. <br>
                    3.) Test license is also required in case of Indian manufacturers.
                    </p>
    

          <p>Description: CDSCO registration or CDSCO licence can be obtained by the process of CDSCO online
            registration on the portal.
          </p>
          <p>CDSCO stands for Central Drugs Standard Control Organisation (which comes under the Ministry of Health and
            Family Welfare), headquartered in Delhi and has 6 zonal offices in India namely at Mumbai, Kolkata,
            Chennai, Ghaziabad, Hyderabad and Ahmedabad and various other sub-zonal offices. It is the national
            regulatory body for products like cosmetics, drugs, medical devices, etc.
          </p>

          <h5 id="1">Different divisions of the CDSCO are as below:

          </h5>


          <p>1.) Medical device division ( Drugs ) <br>
            2.) Drugs <br>
            3.) Cosmetics <br>
            4.) Clinical Trial on medical devices division <br>
            5.) Vaccines <br>
            6.) BA/BE
          </p>



          <h2 class="h1-design heading_design font-size m-t10 text-center" style="text-transform: uppercase;" id="2">
            Who can apply for CDSCO certificate ?
          </h2>
          <!--<p>All manufacturers, either of Indian or foreign origin can apply for the <span style="color:blue">CDSCO-->
          <!--    licence</span>.In case of foreign manufacturer license will be granted to the manufacturer and the Indian-->
          <!--  counterpart of the manufacturer will act as it’s Indian Representative. The Indian representative of the-->

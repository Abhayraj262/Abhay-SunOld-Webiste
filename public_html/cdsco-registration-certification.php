          <p>All manufacturers, either of Indian or foreign origin, can apply for the CDSCO license. In the case of a 
          foreign manufacturer, the license will be granted to the manufacturer, and the Indian counterpart of the manufacturer
          will act as its authorized Indian Representative. The Indian representative of the manufacturer must have a valid state 
          drugs license under Form 20-B/21-B or Form 42 (applied under Form 41) as per the latest guidelines released by CDSCO.</p>

          <h2 class="h1-design heading_design font-size m-t10 text-center" style="text-transform: uppercase;" id="3">
            Classification of medical devices for obtaining CDSCO registration:</h2>
          <p>All types of medical devices are divided into 4 classes by the Medical Device Rules 2017, in 4 main
            categories, namely</p>

          <p> 1. Class A ( least risk ) <br>
            2. Class B ( Moderate risk ) <br>
            3. Class C ( Moderate to high risk ) <br>
            4. Class D ( High risk )
          </p>


          <p>products falling in class A are further divided into 2 categories, to ascertain if <span style="color: blue;"> CDSCO
              license</span> or <span style="color: blue;">CDSCO registration</span> is applicable on the product, below is the division::
          </p>

          <p>a.) Class A ( non sterile and non measuring ) products come under registration scheme. <br>
            b.) Class A ( Sterile or measuring ) products require license.
          </p>

          <br><br>
          <p>This brings us to the conclusion that products which are under class A ( sterile or measuring ), Class B,
            Class C and Class D require a license from CDSCO.
          </p>





          <h2 class="h1-design heading_design m-t10 font-size text-center" style="text-transform: uppercase;" id="4"> Vital
            Documents Required for CDSCO LICENSE:
          </h2>
          <p>A.) CDSCO registration for Class A products ( non-sterile and non-measuring )

          </p>


          <p> 1. Drugs license of importer ( Required only for foreign manufacturer ) <br>
            2. Free sales certificate ( Required only for foreign manufacturer ) <br>
            3. ISO certificate ( Required only for foreign manufacturer ) <br>
            4. List of products to be registered. <br>
            5. Basic details about the product ( example: storage temperature, shelf life, model number, etc.) , etc.
          </p>



          <p>B.) Class A ( sterile or measuring ), Class B, Class C and Class D
          </p>

          <p> 1. Plant Master File ( PMF ) <br>
            2. Device Master File ( DMF ) <br>
            3. Manufacturing process flowchart. <br>
            4. Labels and user manual. <br>
            5. Design verification and validation. <br>
            6. Stability validation data. <br>
            7. Challan of the government fee paid, etc.
          </p>



          <h2 class="h1-design heading_design font-size m-t10 text-center" style="text-transform: uppercase;" id="5">Process for
            CDSCO registration for Class A products ( non-sterile and non-measuring )
          </h2>

          <p>
          <ul>
            <li>Generation of login Credentials.</li>
            <li> Registration of Indian / foreign manufacturer</li>
            <li> Registration of importer ( in case of import only )</li>
            <li> Addition of product details</li>
            <li> Submission of details to CDSCO</li>
            <li> Grant of registration.</li>
            </li>
          </ul>
          </p>



          <p>Timeline : <span style="color:blue">CDSCO registration process</span>will take 1 day after the documents
            are submitted to us.
          </p>

          <p>Validity : Validity of CDSCO online registration is perpetual i.e. no end date and requires no renewal.
          </p>

          <p>Note : After the registration is completed, a unique file number is generated, which has to be mentioned on
            the product / packaging, <b>in case of registration there is no certificate</b></p>


          <h4 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;" id="6">Process for
            CDSCO license :</h4>


          <p>
          <ul>
            <li>Identifying the class of the medical device.
            </li>
            <li>Making groups as per the guidelines mentioned as per CDSCO guidelines.
            </li>
            <li>Creation of login credentials for the manufacturer.
            </li>
            <li>Collection of documents from the overseas manufacturer.</li>
            <li>Collection of documents from Indian importer.
            </li>
            <li>Checking and vetting of documents as per the rules.</li>
            <li>Payment of government fees.
            </li>
            <li>Submission of Application online.
            </li>
            <li>Clearing of queries ( if any )
            </li>
            <li>Audit of the firm.
            </li>
            <li>Clearing of queries ( if any ) after the audit.
            </li>
            <li>Grant of license
            </li>
            <li>Generation of CDSCO certificate.
            </li>
          </ul>

          </p>

          <p>Timeline : Approx 6-8 weeks after submission of application.
          </p>


          <p>Validity : It is granted for 5 years initially and has to be renewed after that by paying the government
            fees.
          </p>

          <p>Note: 1.) In case of CDSCO import license, AUDIT OF THE FOREIGN MANUFACTURER IS NOT CONDUCTED, and rest of
            the procedure remains the same. <br>
            2.) License number mentioned on the CDSCO registration certificate has to be mentioned on the products /
            packaging. <br>
            3.) Test license is also required in case of Indian manufacturers.
          </p>

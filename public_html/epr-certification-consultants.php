<h2 class="h1-design heading_design font-size m-t10 text-center" style="text-transform: uppercase;" id="1">
          EPR Certificate
          </h2>
                    
                    <p>Central Pollution Control Board (CPCB) of India has notified to obtain EPR Certificate for
                        E-Waste
                        Management. All the Indian manufacturers and importers of Electronic and Electrical Products
                        need to submit
                        a future plan of the proper disposal of the Electronic-Waste to Central Pollution Control Board
                        (CPCB) as a mandatory requirement. As per the guidelines, manufacturers and importers needs to
                        tie-up with
                        authorized recycler to dispose the E-Waste and submit all the plans related to estimated dump
                        quantity and
                        method adopted for disposal in CPCB to get the EPR Certificate.
                    </p>







                    <h2 class="h1-design heading_design font-size m-t10 text-center" style="text-transform: uppercase;" id="5">
                        Synopsis for control of E-waste</h2>

                    <p>
                        Proper control over the materials used in the manufacturing process is an important way to
                        reduce waste
                        generation (Freeman, 1989). By reducing both the quantity of
                        hazardous materials used in the process and the amount of excess raw materials in stock, the
                        quantity of
                        waste generated can be reduced. This can be done in two ways
                        i.e. establishing material-purchase review and control procedures and inventory tracking
                        system.Developing
                        review procedures for all material purchased is the
                        first step in establishing an inventory management program. Procedures should require that all
                        materials be
                        approved prior to purchase. In the approval process
                        all production materials are evaluated to examine if they contain hazardous constituents and
                        whether
                        alternative non-hazardous materials are available.Another
                        inventory management procedure for waste reduction is to ensure that only the needed quantity of
                        a material
                        is ordered. This will require the establishment of a strict inventory tracking system. Purchase
                        procedures
                        must be implemented which ensure that materials are ordered only on an as-needed basis and that
                        only the
                        amount needed for a specific period of time is ordered.
                    </p>






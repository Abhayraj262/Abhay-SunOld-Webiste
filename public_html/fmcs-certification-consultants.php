                For the products falling under the mandatory list, FMCS BIS certification is mandatory, the list of products mandatory can be accessed <a href="https://www.bis.gov.in/product-certification/products-under-compulsory-certification/scheme-i-mark-scheme/" style="color: blue !important;">on this link</a> .

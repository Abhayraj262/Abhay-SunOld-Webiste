                <h2 class="h1-design heading_design m-t10 text-center font-size" style="text-transform: uppercase;" id="4">Process of Obtaining BIS license under Foreign Manufacturer Certification Scheme ( FMCS )
                </h2>
                <p>Stage-1: Submission of application to BIS in hard copy </p>
                <p>Stage-2: The application will be scrutinized by BIS officials in-line with the Indian standard ( IS ) and then the shortcomings will be communicated.
                </p>
                <p>Stage-3: Clearing of queries ( if any ) by submitting the reply to BIS.  </p>

                <p>Stage-3: Assignment of the BIS auditor for conducting audit at the manufacturing premises.</p>
                <p>Stage-4: Finalization of audit date with mutual consent of the manufacturer</p>
                <p>Stage-5: Conduction of audit at the manufacturing premises in foreign and sealing of samples by the auditor.</p>
                <p>Stage-6: Testing of sample as per the Indian Standard at the manufacturer’s in house test lab </p>
                <p>Stage-7: Sending of the sealed sample to selected BIS lab in India</p>
                <p>Stage-8: Clearing of queries ( if any ) raised during the audit</p>
                <p>Stage-9: After BIS receives the test report of the sample sent for testing, the license is granted.</p>

                <p><strong>Note: 1.) Performance Bank guarantee must be issued by any bank worldwide that has a RBI approved branch in India. <br>
                        <span>2.) Time to obtain BIS certification for foreign manufacturers is approx. 3 months</span>
              </strong></p>



           <p>Foreign manufacturer Certification scheme ( FMCS ) refers to the scheme of BIS in which product certification for ISI mark is granted for all the foreign manufacturers who want to export their products to india. This scheme was started by the Bureau of Indian Standards in 2000 and till date there are 1,650+ licensees under FMCS certification under various Indian Standards. The department concerned for FMCS is Foreign manufacturer Certification Department (FMCD) and is present at the  headquarters of BIS in New Delhi.</p>
            <p>Foreign manufacturers can take FMCS certification for the products falling under mandatory or voluntary scheme as applicable.As on date over 1000 products fall under the mandatory list and 20,000+ products under voluntary list. Once the certification is obtained by the manufacturer, they have to mention their licence number ( or CM/L No: ) along with other standard details as mentioned in the Indian standard.
            </p>


            <h2 class="h1-design heading_design font-size m-t10 text-center" id="7">Surveillance procedure after grant of BIS certificate:
            </h2>
                <p>BIS conducts surveillance on the manufacturers to ensure that the standards are being followed as per the guidelines.This is done in 2 ways:
                </p>

                <p>1. Market surveillance: 
                </p>

                <p style="margin-left: 20px;">This is done twice yearly. Samples are randomly picked from market and sent to government labs for testing as per the standards, If the sample passes, no problem, but if the sample fails , the manufacturer is informed and they have to take corrective action.Subsequent failure of market sample can lead to suspension or cancellation of the licence.
                </p>

                <p>2. Factory surveillance:
                </p>
                <p style="margin-left: 20px;">After obtaining the licence, there are certain formalities like maintenance of test records, regular testing of the product and so on.To keep a check that all the protocols are followed, BIS conducts random factory surveillance twice in a year to keep a check on the manufacturer. If subsequent mistakes are observed during several visits, the license is liable to be suspended / cancelled.</p>










          

                  

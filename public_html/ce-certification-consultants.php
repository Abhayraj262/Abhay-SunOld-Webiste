                CE Marking of Electronic and Electrical Equipment?:
              </h4>
              <p>The European Commission lifts the curtain from the harmonized conformity with the EU EMC that puts all
                their attention in checking the quality, safety and non- health hazardous substance in the products. The
                EC made CE marking mandatory stamp for the production of electronic and electrical equipment that is
                sold in EEA. This made a particular standard of the above equipment safe and ready to use and make the
                product less harmful to the mankind of the users.

              </p>


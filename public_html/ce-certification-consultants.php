                    var paddingTopPart = parseInt(paddingTop) / 50;
                    var paddingBottomPart = parseInt(paddingBottom) / 30;

                    (function innerFunc(i, t, b) {

                      subMenu.style.height = i + 'px';

                      i += part;

                      if (t < paddingTop) {

                        t += paddingTopPart;
                        subMenu.style.paddingTop = t + 'px';


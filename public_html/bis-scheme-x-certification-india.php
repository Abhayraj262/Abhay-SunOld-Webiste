            <h3 class="m-t10 font-size text-center">Documents Required for BIS Certification Under Scheme X – Omnibus Technical Regulation</h3>
            <p>To comply with the <strong>Omnibus Technical Regulation</strong>, manufacturers of machinery and electrical equipment must obtain BIS certification under Scheme X, a comprehensive conformity assessment scheme governed by the <strong>Bureau of Indian Standards (BIS)</strong>. This certification is mandatory for both domestic and imported products listed in the First Schedule of the Regulation and ensures adherence to strict technical and safety standards in line with global and Indian standards.</p>
            <p>Whether you're manufacturing transformers, compressors, control gear, motors, switchgear, or pumps, obtaining a valid <strong>BIS licence</strong> or <strong>BIS certificate</strong> under Scheme X is essential for legal sale, import, or distribution in the Indian market.</p>


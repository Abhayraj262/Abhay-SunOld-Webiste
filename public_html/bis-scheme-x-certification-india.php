            </ul>

<h3 class="h1-design heading_design m-t10 font-size text-center">Step 3: Compile a Detailed Technical File</h3>
            <p>A core requirement for Scheme X certification is the <strong>Technical File</strong>, which enables BIS to assess product conformity. The file should include:</p>
            <ul>
              <li><strong>Design features and working principles of the product</strong></li>
              <li><strong>Manufacturing processes and in-line quality control procedures</strong></li>
              <li><strong>Raw materials used, third-party or in-house testing protocols</strong></li>
              <li><strong>Supplier/subcontractor information and inspection reports</strong></li>
              <li><strong>Evidence of compliance with applicable Indian standards</strong></li>
            </ul>

            <h3 class="h1-design heading_design m-t10 font-size text-center">Step 4: Submit Application via Manak Online</h3>
            <p>For Indian manufacturers, the application must be submitted online through the <strong>Manak Online</strong> portal, the official platform of the Indian BIS. The application must include:</p>
            <ul>
              <li><strong>Upload of all required documents</strong></li>
              <li><strong>Payment of the applicable government and inspection fees</strong></li>
            </ul>
            <p><strong>Note:</strong> For foreign manufacturers, the application process is offline, and documents must be submitted physically to BIS Headquarters in India.</p>

            <h3 class="h1-design heading_design m-t10 font-size text-center">Step 5: Factory Audit and Inspection</h3>
            <p>Upon review of your application, BIS officials will conduct a factory inspection to verify conformity with the Omnibus Technical Regulation. The duration typically depends on your manufacturing location:</p>
            <ul>
              <li><strong>2 days for Indian manufacturers</strong></li>
              <li><strong>3 days for overseas manufacturers</strong></li>
            </ul>

            <h4 class="h1-design heading_design m-t10 font-size text-center">Step 6: Product Sample Testing</h4>
            <p>After the inspection, the product sample is tested to verify compliance with the relevant Indian standard. Testing is conducted either:</p>
            <ul>
              <li><strong>At the manufacturing site (in-house lab)</strong></li>
              <li><strong>In a BIS-accredited external laboratory</strong></li>
            </ul>

<h5 class="h1-design heading_design m-t10 font-size text-center">Step 7: Grant of BIS Certificate</h5>
            <p>If the sample passes all tests and the inspection is satisfactory, the Indian BIS will issue a <strong>BIS licence</strong> or <strong>BIS certificate</strong> under Scheme X. Once granted, manufacturers are authorized to affix the <strong>BIS standard mark</strong> on their certified products.</p>
            <p>This mark differs from the ISI mark, which is typically used for standardized consumer goods. The BIS mark under Scheme X is intended for machinery, electrical systems, and industrial equipment certified as compliant with advanced Indian standards.</p>

            <h2 class="h1-design heading_design font-size m-t10 text-center" id="7">Why Scheme X Certification Matters</h2>
            <ul>
              <li><strong>Confirms compliance</strong> with stringent technical and safety standards</li>
              <li><strong>Builds market credibility</strong> and consumer trust</li>
              <li><strong>Enables legal sale</strong> and import of products in India</li>
              <li><strong>Allows use of the BIS standard mark</strong>, a symbol of quality and conformity</li>

  (function () {
    const accordions = document.querySelectorAll('.accordions');
    const activeClass = 'accordions__link_active';
    const timer = 100;

    accordions.forEach((accordion) => {
      accordion.addEventListener('click', function (e) {
        if (e.target.tagName !== 'A') return;

        const link = e.target;
        const subMenu = link.nextElementSibling;

        if (!subMenu || !subMenu.classList.contains('sub-accordions')) return;

        const isActive = link.classList.contains(activeClass);

        // Toggle current submenu
        if (isActive) {
          slide(link).up(() => {
            link.classList.remove(activeClass);
          }, timer);
        } else {
          // Close any open submenu in the same group
          const openLink = accordion.querySelector('.' + activeClass);
          if (openLink) {
            slide(openLink).up(() => {
              openLink.classList.remove(activeClass);
            }, timer);
          }

          // Open current submenu
          link.classList.add(activeClass);
          slide(link).down(() => {}, timer);
        }
      });
    });
  })();

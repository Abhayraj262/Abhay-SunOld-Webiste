                <h5>Application fees</h5>
              </li>
              <li>
                <h5>Certification and inspection fees</h5>
              </li>
              <li>
                <h5>Testing and sample procurement charges</h5>
              </li>
            </ul>
            <p>Additionally, the AIR assumes <strong>legal responsibility</strong> for ensuring ongoing compliance with Scheme X regulations, making them liable in the event of non-compliance or misuse of the BIS standard mark.</p>

            <h4 class="h1-design heading_design font-size m-t10 text-center">5. Post-Certification Responsibilities</h4>
            <p>Once the BIS certificate or licence is granted, the AIR continues to:</p>
            <ul>
              <li>
                <h5>Monitor post-certification compliance</h5>
              </li>
              <li>
                <h5>Ensure correct labeling and usage of the BIS standard mark (specific to Scheme X)</h5>

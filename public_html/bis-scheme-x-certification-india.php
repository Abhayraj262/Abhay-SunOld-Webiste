            <h2 class="h1-design heading_design font-size m-t10 text-center">Role of Authorized Indian Representative (AIR) in BIS Scheme X Certification</h2>
            <p>For foreign manufacturers aiming to enter the Indian market under the Omnibus Technical Regulation, appointing an <strong>Authorized Indian Representative (AIR)</strong> is a mandatory requirement set by the Bureau of Indian Standards (BIS). The AIR acts as the official point of contact within India, representing the manufacturer throughout the BIS certification process under Scheme X.</p>
            <p>This requirement ensures that the Indian BIS has a legally accountable and locally accessible entity to manage compliance, certification, and communication obligations on behalf of the foreign applicant.</p>

            <h2 class="h1-design heading_design m-t10 font-size text-center">Key Responsibilities of the AIR in BIS Scheme X Certification</h2>

            <h5 classs="n font-size m-t10 text-enter">1. Managing BIS Registration & Communication</h5>
            <p>The <strong>Authorized Indian Representative (AIR)</strong> is responsible for initiating and overseeing the BIS registration process under Scheme X, including creating and maintaining the application. All official communication with BIS authorities, including clarification requests, inspection coordination, and certification updates, is handled through the AIR.</p>

            <h4 class="h1-design heading_design font-size m-t10 text-center" id="9">2. Document Submission and Compliance Management</h4>
            <p>The AIR submits all necessary documentation, including:</p>
            <ul>
              <li>
                <h5>Technical files</h5>
              </li>
              <li>
                <h5>Product specifications</h5>
              </li>
              <li>
                <h5>Test reports</h5>
              </li>
              <li>
                <h5>Compliance declarations</h5>
              </li>
            </ul>
            <p>These are crucial for obtaining a BIS licence or BIS certificate under Scheme X, ensuring the product aligns with applicable Indian standards.</p>

            <h4 class="h1-design heading_design font-size m-t10 text-center">3. Coordination of Factory Inspections</h4>
            <p>During the factory inspection phase conducted by BIS officials, the AIR facilitates:</p>
            <ul>
              <li>
                <h5>Scheduling and organizing inspection visits</h5>
              </li>
              <li>
                <h5>Assisting BIS auditors during on-site evaluations</h5>
              </li>
              <li>
                <h5>Providing access to production lines, testing areas, and quality control units</h5>
              </li>
            </ul>
            <p>This is a critical step before the BIS certification can be approved.</p>

            <h4 class="h1-design heading_design font-size m-t10 text-center">4. Handling Fees and Legal Accountability</h4>
            <p>The AIR is authorized to manage all associated fees, such as:</p>
            <ul>

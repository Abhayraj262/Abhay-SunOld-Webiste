            <!--<h1>BIS Certification Under Scheme X: A New Era of Quality Compliance for Industrial Equipment</h1>-->

            <p>India is stepping up its quality and safety standards with the implementation of <strong>Scheme X Certification</strong>, a key component of the <strong>BIS Conformity Assessment Regulations, 2018</strong>—further strengthened by the <strong>2022 Amendment</strong>. This scheme is not merely a formal compliance requirement but a progressive framework designed to bring both domestic and imported products in line with globally recognized technical and safety norms. <strong>Scheme X Certification</strong> places a strong emphasis on mechanical and electrical equipment, including but not limited to <strong>pumps, compressors, motors, transformers, switchgears, cranes</strong>, and other machines. For these product categories, securing a BIS certificate is now compulsory, serving as a clear indicator of conformity to some of the highest safety and quality benchmarks in the world.</p>

            <h2 class="h1-design heading_design font-size m-t10 text-center">Exemptions and Special Provisions under Scheme X of BIS Certification</h2>

            <p><strong>Scheme X</strong> introduces a focused approach for construction machinery, particularly those governed by the <strong>Central Motor Vehicles Rules (CMVR), 1989</strong>. In collaboration with the <strong>Ministry of Road Transport and Highways (MoRTH)</strong>, a customized compliance mechanism has been developed specifically for these high-capacity machines. This sector-specific protocol ensures that even the heaviest and most complex construction equipment adheres to stringent safety and performance standards. Through this strategic inclusion, Scheme X not only enhances regulatory oversight but also reinforces the BIS’s commitment to public safety, infrastructure quality, and dependable product performance—whether sourced domestically or imported.</p>

            <h2 class="h1-design heading_design font-size m-t10 text-center">Omnibus Technical Regulation and BIS Certification Under Scheme X Certification</h2>

            <p>The <strong>Omnibus Technical Regulation</strong>, formally known as the <strong>"Machinery and Electrical Equipment Safety (Omnibus Technical Regulation) Order, 2024"</strong>, marks a significant leap in India’s regulatory framework for industrial products. Issued by the <strong>Ministry of Heavy Industries</strong> on <strong>August 28, 2024</strong>, this regulation mandates <strong>BIS certification under Scheme X</strong> for a broad category of machinery and electrical equipment, whether manufactured locally or imported into India. While originally slated for enforcement from <strong>August 28, 2025</strong>, the implementation date has now been extended to <strong>1st September 2026</strong>.</p>

            <p>This regulation plays a crucial role in aligning Indian BIS standards with global safety benchmarks. Covering everything from <strong>pumps, compressors, centrifuges, motors, transformers, switchgear</strong>, and other mechanical or electrical assemblies, the Omnibus Order ensures that only products meeting stringent Indian standards are permitted for sale in the Indian market. <strong>BIS certification under Scheme X</strong> is now compulsory for these categories, enhancing both industrial safety and consumer trust.</p>

            <h2 class="h1-design heading_design font-size m-t10 text-center">Exemptions to Note</h2>

            <p>However, not all products fall under the scope of this regulation. <strong>Products already covered under separate Quality Control Orders (QCOs)</strong>, issued via <strong>Section 16 of the BIS Act, 2016</strong>, are exempt. Additionally, <strong>machinery intended solely for export</strong> and <strong>construction equipment governed by the CMVR Rules, 1989 (under MoRTH)</strong> are excluded from this mandatory registration.</p>

            <h2 class="h1-design heading_design font-size m-t10 text-center">Scheme X: Pathway to BIS Licence or Certificate of Conformity (CoC)</h2>

            <p>Under <strong>Scheme X</strong>, manufacturers and importers must secure either a <strong>BIS licence</strong> or a <strong>Certificate of Conformity (CoC)</strong> from the <strong>Bureau of Indian Standards</strong>. Both certifications allow the use of the <strong>ISI mark</strong>, indicating full compliance with applicable technical and safety standards. The <strong>BIS certificate</strong> issued under this scheme serves as a powerful assurance of <strong>quality, safety, and reliability</strong>—key parameters for both industrial operations and consumer protection.</p>

            <p>This scheme is part of a broader movement by Indian BIS to enforce consistency and compliance across high-risk product categories. By requiring <strong>BIS registration under Scheme X</strong>, the Indian government is ensuring that all regulated machinery and electrical equipment undergo standardized testing and inspection.</p>

            <h2 class="h1-design heading_design font-size m-t10 text-center">Driving Compliance, Safety, and Global Competence</h2>

            <p>The <strong>Omnibus Technical Regulation</strong> is more than just another compliance requirement. It’s a transformative reform that will reshape the landscape for <strong>machine tools, electrical systems, motors, cranes</strong>, and similar heavy-duty equipment in India. It enables Indian manufacturers to meet international standards, fosters domestic manufacturing excellence, and strengthens the BIS compliance ecosystem.</p>

            <h2 class="h1-design heading_design font-size m-t10 text-center">Difference Between Scheme X Certification and ISI Mark: A Complete Guide to Indian BIS Certification Systems</h2>

            <p>When discussing product certification in India, two commonly referenced terms are <strong>Scheme X</strong> and the <strong>ISI mark</strong>. Though both are governed by the <strong>Bureau of Indian Standards (BIS)</strong>, they cater to different product categories and follow distinct certification protocols. For manufacturers, importers, and businesses pursuing BIS certification, understanding these differences is critical for regulatory compliance and market entry.</p>

            <h3 class="h1-design heading_design m-t10 font-size text-center" id="1">What is the ISI Mark?</h3>

            <p>The <strong>ISI mark</strong> represents a long-standing certification system under the Indian BIS framework, aimed at confirming that products meet predefined Indian standards. Products carrying the ISI mark assure consumers of consistent quality, safety, and performance. This certification is <strong>mandatory for several consumer goods</strong> such as <strong>electrical appliances, pressure cookers, toys, and helmets</strong>. The ISI mark is issued only after thorough evaluation and factory audits to ensure product compliance with relevant Indian standards. A <strong>BIS licence</strong> is required to affix the ISI mark.</p>

            <h3 class="h1-design heading_design m-t10 font-size text-center">What is Scheme X Certification?</h3>

            <p><strong>Scheme X</strong> is a newer, more advanced conformity assessment scheme introduced under the <strong>BIS Conformity Assessment Regulations, 2018</strong>. It specifically targets <strong>industrial machinery, complex electrical equipment</strong>, and <strong>high-risk products</strong> that require in-depth technical evaluation. Products falling under Scheme X must either obtain a <strong>BIS certificate</strong> or a <strong>BIS licence</strong>, depending on whether they are imported or domestically manufactured.</p>

            <p>One of the unique aspects of Scheme X is the availability of a <strong>Certificate of Conformity (CoC)</strong>, making it highly relevant for imported machinery and industrial equipment. Certification under Scheme X involves <strong>rigorous testing, safety verification</strong>, and <strong>periodic surveillance</strong> to ensure ongoing compliance with high-end technical and safety standards.</p>


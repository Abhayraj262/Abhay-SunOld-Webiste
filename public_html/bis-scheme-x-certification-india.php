            <p>Under Scheme X, the Indian BIS does not permit direct engagement with foreign entities. Without a registered <strong>Authorized Indian Representative</strong>, a foreign manufacturer cannot apply for or obtain BIS certification under the <strong>Omnibus Technical Regulation</strong>. The AIR acts as a regulatory bridge—ensuring that all imported products meet India's safety, quality, and technical compliance requirements.</p>
            <p>It's important to note that while the <strong>ISI mark</strong> is traditionally used for consumer goods, products certified under Scheme X must carry the appropriate <strong>BIS standard mark</strong>, and it is the AIR’s duty to ensure its correct usage as per BIS guidelines.</p>


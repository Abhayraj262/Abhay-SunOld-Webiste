  function slide(link) {
    var down = function (callback, time) {
      var subMenu = link.nextElementSibling;
      subMenu.style.display = 'block'; // Critical fix
      var height = subMenu.scrollHeight;
      var part = height / 100;

      var paddingTop = parseInt(window.getComputedStyle(subMenu).paddingTop);
      var paddingBottom = parseInt(window.getComputedStyle(subMenu).paddingBottom);
      var paddingTopPart = paddingTop / 50;
      var paddingBottomPart = paddingBottom / 30;

      (function innerFunc(i, t, b) {
        subMenu.style.overflow = 'hidden';
        subMenu.style.height = i + 'px';

        i += part;

        if (t < paddingTop) {
          t += paddingTopPart;
          subMenu.style.paddingTop = t + 'px';
        } else if (b < paddingBottom) {
          b += paddingBottomPart;
          subMenu.style.paddingBottom = b + 'px';
        }

        if (i < height) {
          setTimeout(() => innerFunc(i, t, b), time / 100);
        } else {
          subMenu.removeAttribute('style');
          callback();
        }
      })(0, 0, 0);
    };

    var up = function (callback, time) {
      var subMenu = link.nextElementSibling;
      var height = subMenu.clientHeight;
      var part = height / 100;

      var paddingTop = parseInt(window.getComputedStyle(subMenu).paddingTop);
      var paddingBottom = parseInt(window.getComputedStyle(subMenu).paddingBottom);
      var paddingTopPart = paddingTop / 30;
      var paddingBottomPart = paddingBottom / 50;

      (function innerFunc(i, t, b) {
        subMenu.style.overflow = 'hidden';
        subMenu.style.height = i + 'px';

        i -= part;
        i = i.toFixed(2);

        if (b > 0) {
          b -= paddingBottomPart;
          subMenu.style.paddingBottom = b + 'px';
        } else if (t > 0) {
          t -= paddingTopPart;
          subMenu.style.paddingTop = t + 'px';
        }

        if (i > 0) {
          setTimeout(() => innerFunc(i, t, b), time / 100);
        } else {
          subMenu.removeAttribute('style');
          callback();
        }
      })(height, paddingTop, paddingBottom);
    };

    return {
      down: down,
      up: up
    };
  }


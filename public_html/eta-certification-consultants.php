                <li>The Radio Frequency Tests should be conducted in an Authorized Indian Laboratory</li>
                <li>Preparation of all needed documents for the applications</li>
                <li>Evaluation of the application</li>
                <li>Generating online Portal at Saal Sanchar Portal</li>
                <li id="2">Online or Offline submission of the application with the required documents along with the Radio
                  Frequency Test Results to WPC</li>
                <li>WPC issuing the certificate</li>
              </ul>

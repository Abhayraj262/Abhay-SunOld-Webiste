            <p><b>WPC ETA Certificate in India </b></p>
            <p id="">To encourage the import of wireless devices into India, Equipment Type Certification (ETA) is essential;
              the WPC department must get this approval. Products that operate in the de-licensed frequency band without
              WPC certification must have an ETA certificate. Mobile phones, RFID, Bluetooth gadgets, Wi-Fi
              technologies, and other items are included in the WPC's Equipment Type Approval (ETA) scope.</p>
            <p>It is not necessary to get a license from WPC to operate devices in certain frequency bands because
              certain bands have been given license-free status. However, a thorough test report of the equipment from a
              reputable laboratory must also be provided to WPC for consideration to receive an ETA.</p>
            <p>For the process of any type of radio-frequency-enabled equipment, authorization is necessary. The WPC
              department must give its approval before any radio-based frequency equipment can be imported into India.
              This organization would guarantee that the service provider complied with all licensing requirements. The
              Department of Telecommunications controls wireless coordination and planning for the wing (DoT). The
              license criteria for wireless testing and frequency testing would fall within the purview of this
              division.</p>

            <p class="h7">What is WPC?</p>
            <p>The Government of India created a division in 1952 called The Wireless Planning & Coordination (WPC). WPC
              is a division of the Department of Telecommunications. This was set up by the Indian Government Ministry
              of Communications and Information Technology. The National Radio Regulatory Authority, or WPC, is in
              charge of policing the frequency spectrum. WPC Office issues WPC (ETA) Licenses for IT and electronic
              equipment to meet the demands of wireless technology users. Only items that have received prior WPC
              clearance can be sold in the Indian market. This also includes wireless and<span id=""> Bluetooth</span> devices and IT and
              electronic equipment that adhere to human safety standards.</p>
            <p class="h7">What is ETA? </p>
            <p>Equipment Type Approval (ETA) is needed to enhance the import of wireless devices into India. We must get
              this approval from the WPC department. The ETA from WPC covers Mobile phones, RFID, Bluetooth gadgets,
              Wi-Fi technologies, and other items. Since the frequency band has been license-free, a product operating
              in that range is not necessary to apply for a WPC license. However, a document, including a thorough test
              report of the equipment purchased from a reputable laboratory, must be presented to WPC for representation
              to get an ETA Licence.</p>
            <p class="h7">WPC Approval in India</p>
            <p>WPC ETA certificate is must for all the electronic products in the Indian market that use Wi-Fi and
              Bluetooth. The ETA must receive WPC approval before electronic devices are ready for sale in the Indian
              marketplace. These devices use unlicensed frequency bands to operate.</p>
            <p><b>WPC issues Different Types of Licences and they are as listed below:</b></p>
            <ol class="m-l30 m-t10  text-justify">
              <li>Equipment Type Approval</li>
              <li>Type Approval</li>
              <li>Experimental Approval</li>
              <li>Dealer Possession Licence</li>
              <li> Non- Dealer Possession Licence </li>
              <li>Import License for radio and gadgets </li>
            </ol>
            <p class="h7">Licencing & Regulations</p>
            <p>When it comes to licensing and requirements for WPC approval, the department will be taken into
              consideration. All tasks pertaining to the licensing and regulation of the WPC fall under the
              responsibility of this authority.</p>
            <h2 class="h1-design heading_design m-t10 text-center">
              Types of WPC Approval / WPC Certification
            </h2>
            <p><b id="3">WPC Certification- Wireless Planning and Coordination certification is mainly of 3
                major types which are listed as below:</b></p>
            <div class="imgpesa"><img src="img/isi-msrk-img/wpc 2.jpg" alt="Petroleum and Explosive Safety Organization" class="img-fluid"></div>
            <p class="h7"><b>1</b> Non-Network Licence</p>

            <p>Non-network service providers, such as cable TV distributors and manufacturers of wireless devices, are
              granted this license. To sell, buy, and import certain wireless equipment, you need this license. The WPC
              will only grant you a non-network license if you are an incorporated company entity with the right
              personnel for your tools or business. There are three different kinds of it</p>

            <div class="border3d ">
              <p class="h7">Import Licence</p>
              <p>This license is issued to export wireless products to India. The owner of this licence will get
                permission to sell his wireless products in entire India. </p>
              <p class="h7">Dealer Possession Licence </p>


              <p>A person or organization that sells and distributes wireless products is given a Dealer Possession
                Licence by WPC.</p>
              <p class="h7">Non- Dealer Possession Licence</p>
              <p>A person or organization that does not deal in wireless devices and does not already have a license
                from WPC is given a non-Dealer Possession Licence.</p>
              <p class="h7"><b>2</b> Network Licence</p>

              <p>An organization that want to use the nation's constrained radio frequency capacity to offer
                communication services, such as mobile networks, cable, and DTH, must get a network license. There are
                two types of </p>
              <p class="h7">Internet Service Provider Licence (ISP)</p>
              <p>Any business that wishes to offer internet services must obtain an ISP license from the Department of
                Telecommunications (DoT).</p>
              <p class="h7">Experimental Licence</p>
              <p>Any company or individual wishing to perform tests or research on wireless devices must first get an
                experimental license from the WPC Department.</p>
              <p class="h7"><b>3</b> Equipment Type Approval (ETA)</p>
              <p id="4">We should constantly ask ourselves a question before importing something from outside. What regulations
                must my product follow? Ensure that the equipment has a WPC ETA Certificate, especially if it is a
                Bluetooth device or RF ID-based equipment. </p>

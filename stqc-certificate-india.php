            <h2 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;">STQC Audit & STQC Audit Process</h2>

            <p>The <strong>STQC Audit</strong> is one of the most critical components of certification.</p>

            <h3 style="color:#00796b; font-size:22px; margin-top:20px;">What is STQC Audit?</h3>
            <p>It is a structured evaluation of your system's:</p>
            <ul class="p-design" style="margin-left:20px;">
              <li>Security</li>
              <li>Performance</li>
              <li>Compliance</li>
              <li>Documentation</li>

            <h3 style="color:#00796b; font-size:22px; margin-top:20px;">STQC Audit Process</h3>

            <h4 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;">1. Pre-Audit Preparation</h4>
            <ul class="p-design" style="margin-left:20px;">
              <li>Document review</li>
              <li>System readiness check</li>

              <li> Importer Exporter Code (IEC)</li>
              <li> GST Registration Certificate </li>
              <li>Proof of Identity & Address </li>
              <li> Passport Size Photograph of Applicant</li>
              <li>Aadhaar Number of applicant / authorized signatory</li>
              <li>Sample of declaration (label) to be placed on the commodity</li>
              <li> Registered partnership deed in case of partnership firm/ memorandum of articles in case of company
              </li>
              <li id="5"> Commercial data contains sales data for the previous three years, a list of Indian clients,
                and installation details for hazardous areas, such as the equipment's intended use and installation
                location.</li>
            </ol>

            <h3>However, depending on the state where the registration is being looked for, additional documents might
              be required, such as:</h3>
            <p class="p-design  text-justify">However, depending on the state where the registration is being looked
              for, additional documents might be required, such as:</p>
            <ol class="m-l30 m-t10 p-design text-justify">
              <li>Documentary proof of ownership/ lease of premises</li>
              <li>Proof of death of birth of applicant</li>
              <li>Location map of premises</li>
              <li >No objection certificate from Pollution Control Board or any such competent authority</li>

            </ol>
            <p>According to the regulations, the application must be submitted 90 days after the import process started.
              You must pay a charge of 500 INR.
            </p>
            <p>The registering authority will inspect the importer's facilities after receiving a fully completed
              application form, and if everything is in order, will register the applicant and issue them with an LMPC
              certificate. Normally, this takes up to 20 days.
            </p>
            <p >Within seven days of receiving the application, it will be returned to the applicant if it is unfinished.
            </p>
            <p >By paying a charge of INR 100 to the registering body, the importer may modify the certificate.</p>
            <p class="h7">Why Importers need LMPC Certificate? </p>
            <p id="3">If you don't have an LMPC certificate or don't follow the declaration guidelines, customs may hold your
              imported products. A customs seizure might cause delays, costs, and other problems, so be careful to
              register as an importer before your products arrive in India.
            </p>

            <p class="h7"> Latest Amendments under the Legal Metrology Package Commodity Rules, 2011</p>
            <p>The Legal Metrology Package Commodity Rules, 2011, were amended by the government on November 2, 2021,
              and that took to effect on April 1, 2022, prospectively. The Government has</p>
            <p>added provisions to the Law to promote transparency for the product customer under the aforementioned
              revisions. The industry has now changed the labelling and packaging of its products as a result of these
              revisions. Make an appointment with our specialists to receive labelling advice.</p>
            <p class="h7">Rule Violations and Penalties </p>
            <p>A fine shall be assessed for any infraction of the Legal Metrology (Packaged Commodity) Rules.</p>
            <p>The penalty for breaking any of rules 27 to 31 is INR 4,000.</p>
            <p>The fine is INR 2,000 for violating any additional restrictions for which neither the Act nor the Rules
              specify a penalty.</p>
            <p>A late fee will be assessed if your application for an LMPC certificate is submitted more than 90 days
              after the importation process began (INR 5,000 in Delhi)</p>
            <p id="6">If it is not a first offense, there may be additional punishment in addition to the penalty. Typically,
              the maker is responsible. However, if the terms "made by" or "packed by" are absent from the name and
              location of the company indicated on the label, it is assumed that the manufacturer owns the information,
              and culpability will be decided as such. Importers need to be aware of this because any infractions may
              result in them being held accountable.</p>


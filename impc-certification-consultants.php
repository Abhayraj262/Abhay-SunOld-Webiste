            <p><b>LMPC Certification  </b></p>
            <p>In India, the import, sale, and distribution of pre-packaged goods are all governed by the Legal
              Metrology Act 2009 and Legal Metrology (Packaged Commodities) Rules, 2011. A pre-packaged product is any
              item assembled in advance and packaged without any customer. A significant impression of consumer
              awareness and a decrease in unfair trade practices involving such products are created by registration
              under this Act. The Act regulates the exchange of items distributed or sold by weight, measure, or number.
              The Act aims to encourage fair trade practices and safeguard consumer rights by establishing stringent
              rules and regulations, notably for pre-packaged goods.</p>
            <p class="h7">What are Pre-Packaged Goods?</p>
            <p>According to the Act, a pre-packaged good is "put in a packaging of any sort, whether sealed or not, such
              that the product has a pre-determined quantity" without the customer being present. Pre-packaged products
              include, for instance, baby food, gasoline, food items, paint, bagged cement, cables, wires, chemicals,
              wool, and so forth.</p>

            <p class="h7">Underlining the Importance of Legal Metrology</p>
            <p>Metrology is a science of measurement, to put it simply. The major highlight of Legal Metrology is on
              limitations placed on the trade of items classified as "weights and measures," such as thermometers and
              weighing scales. Legal Metrology governs the trade of packaged items measured by weight, measure, or
              number. </p>
            <p>Customers have a right to information about package weight, price, expiration date, and other items when
              they buy pre-packaged goods from the market. Therefore, legal metrology is crucial for advancing ethical
              business operations.</p>
            <p>Below-mentioned is some significant aspects highlighting the significance of legal metrology in modern
              trade:</p>
            <p class="h7">Promoting Trade by Eliminating Disputes</p>

            <p>Trade conflicts are eliminated when the measurement is subject to regulatory monitoring. For instance, if
              a customer purchases 1 kilogram of lentils, they should receive exactly that amount and nothing less.
              Legal metrology also ensures catering to international standards using fundamental measuring tools.</p>
            <p class="h7">Appropriate Management of Inventory</p>
            <p>Correct commodity evaluation procedures give manufacturers a precise breakdown of their available
              inventory. This improves stock control, reduces fraud, and helps manage stock systems more effectively.
            </p>
            <p class="h7">Collection of Government Revenues</p>
            <p>The collection of excise and customs duties, which comprise most of the government's revenue, depends
              critically on measurement. Products made in the nation are subject to excise duty, while commodities
              imported from other manufacturing countries are subject to customs duty. </p>
            <p id="2">Therefore, accurate product weighing enables the legitimate collection of these government revenues. </p>
            <h2  class="h1-design heading_design m-t10 text-center">
              What is an LMPC certificate for importers?
            </h2>
            <p>The Controller of Legal Metrology in the state or the Director of Legal Metrology in the federal
              government must receive an application from traders who intend to import pre-packaged goods for sale or
              distribution in order to have their name and address registered. If the application is approved, the
              director/controller of legal metrology (the registering authority) will register the importer's name and
              address and issue them an LMPC certificate. Here is an example of an LMPC certificate. The duration of an
              LMPC registration ranges from one year to five years.</p>
            <p>You must submit an application to the controller in that state if you want to sell your pre-packaged
              items in just that state. You must register with the director at the Centre through your regional
              controller if you intend to sell in numerous states.
            </p>
            <p class="h7">Responsibilities of an LMPC-certified Importer/Manufacturer</p>
            <p>All pre-packaged goods must include a declaration from the importer or producer that includes the
              following details:</p>
            <p> Address and Name of manufacturer and importer<br />
              Name of Goods contained in the package <br />
              Country of import
              <br />
              Total quantity of with respect to standard units of weights or measurement<br />
              Date of import
              <br />
              Manufacturing Date<br />
              Maximum Retail Price (MRP)<br>
              Dimension of goods<br>
              Contact details of a concerned person or office in case of any consumer complaints
            </p>

            <!--  -->
            <p>The declaration must adhere to a predetermined structure in order to be presented on the box, whether it
              is printed in Hindi or English.</p>
            <p>The Legal Metrology Packaged Commodity Rules include fines of up to Rs. 4,000 for violations.
              Additionally, a late fee will be assessed if an importer requests for an LMPC certificate more than 90
              days after beginning imports.
            </p>
            <p class="h7">Where Should We Apply for LMPC Certificate?</p>
            <p>The Legal Metrology Department in Delhi has an online application platform for applicants who have a
              business unit there. Manufacturers, importers, and packers must pay a registration cost of Rs. 500;
              dealers and repairers must pay a registration charge of Rs. 100. However, there are a few crucial factors
              to take into account before sending the form:</p>
            <ul style="margin-inline:11px ;">
              <li>All essential documents must be uploaded in PDF format and self-attested; the file size should not
                exceed 2 mb.</li>
              <li>
                Jpg is the preferred file type for passport-size images.
              </li>
            </ul>


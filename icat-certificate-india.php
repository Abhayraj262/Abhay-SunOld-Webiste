            <h2 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;" id="2">What is ICAT Certificate?</h2>
            <p>The ICAT Certificate is an official approval issued by the International Centre for Automotive Technology (ICAT), which validates that a vehicle or automotive component complies with Indian regulatory standards.</p>

            <h3 style="color:#00796b; font-size:22px; margin-top:20px;">ICAT Full Form:</h3>
            <p><strong>ICAT</strong> - International Centre for Automotive Technology</p>
            <p>ICAT is one of India's premier automotive testing and certification bodies, operating under the National Automotive Testing and R&amp;D Infrastructure Project (NATRiP), governed by the Ministry of Heavy Industries.</p>

            <h4 class="h1-design heading_design m-t10 text-center" style="text-transform: uppercase;" >Key Highlights of ICAT Certification:</h4>
            <ul class="p-design" style="margin-left:20px;">
              <li>Ensures compliance with Automotive Industry Standards (AIS) and Central Motor Vehicle Rules (CMVR)</li>
              <li>Mandatory for certain automotive products before entering the Indian market</li>
              <li>Applicable to manufacturers, importers, and OEMs</li>
              <li>Covers testing, validation, and certification</li>

          <p>A Sheet Glass is made using a series of rollers that draws the molten glass vertically up out of the mixing tank (where the glass is still a liquid). The glass is rolled up vertically, and then as it is cooling, turned horizontally, for further cooling and processing. Sheet glass has historically been used for thinner “flat glass” products. The sheet process has limitations regarding thickness and overall size as well as clarity but was less expensive than the old plate glass grinding process so it became more commonly used in situations where a thinner, typically lower cost but lower quality glass could be used. </p>



